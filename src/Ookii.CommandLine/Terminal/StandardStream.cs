namespace Ookii.CommandLine.Terminal
{
    /// <summary>
    /// Represents one of the standard console stream.
    /// </summary>
    public enum StandardStream
    {
        /// <summary>
        /// The standard output stream.
        /// </summary>
        Output,
        /// <summary>
        /// The standard input stream.
        /// </summary>
        Input,
        /// <summary>
        /// The standard error stream.
        /// </summary>
        Error
    }
}
